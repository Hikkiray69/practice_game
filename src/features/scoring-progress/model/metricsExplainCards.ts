export function buildMissionMetricsExplainCard(input: {
  mission: QuestScenario;
  choice: QuestChoice;
  consequence: QuestConsequence;
}): MetricsExplainCardData {
  const { mission, choice, consequence } = input;
  const seed = `${consequence.id}:${choice.id}`;
  const vi = pickVariant(seed, 2);
  const body = CHOICE_DEEP[consequence.id]?.[vi] ?? "Этот выбор зафиксирован в сценарии: метрики внизу отражают его последствия в учебной модели.";

  const other = mission.choices.find((c) => c.id !== choice.id);
  const branch =
    other != null
      ? `Вторая ветка («${other.label}») — другой тип компромисса; имеет смысл пройти миссию снова и сравнить смысл, не только цифры.`
      : "Повтор миссии с другим вариантом покажет другой смысл компромисса.";

  return {
    title: "Пояснение к метрикам",
    lines: [`Твой выбор: «${choice.label}».`, body, branch],
  };
}
