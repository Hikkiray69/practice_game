function pickVariant(seed: string, variants: number): number {
  let h = 0;
  for (let i = 0; i < seed.length; i++) {
    h = (h * 33 + seed.charCodeAt(i)) >>> 0;
  }
  return h % variants;
}

type CampaignPosture = "tempo" | "quality" | "balanced" | "integrator" | "strained";

function inferCampaignPosture(quality: number, speed: number): CampaignPosture {
  const gap = speed - quality;
  if (quality >= 10 && speed >= 6) {
    return "integrator";
  }
  if (quality < 2 && speed > 6) {
    return "tempo";
  }
  if (speed < 2 && quality > 6) {
    return "quality";
  }
  if (gap >= 5) {
    return "tempo";
  }
  if (gap <= -5) {
    return "quality";
  }
  if (quality <= 4 && speed <= 4) {
    return "strained";
  }
  return "balanced";
}

