function minigameFootnote(earned: number): string {
  if (earned === 3) {
    return "Миниигры: все три закрыты — моторика и внимание тоже прокачаны, не только «голова».";
  }
  if (earned === 0) {
    return "Миниигры не закрывались — картина по решениям есть, но телесная отработка темпа и фокуса остаётся на добор.";
  }
  return `Миниигры: ${earned}/3 с полным проходом — часть навыков в зоне «напомни себе руками», не только в диалогах.`;
}

export function buildCampaignMetricsFullSheet(input: {
  totals: { score: number; quality: number; speed: number };
  campaignStars: CampaignStarSlot[];
}): MetricsExplainCardData {
  const earned = input.campaignStars.filter((s) => s === "earned").length;
  const { score, quality, speed } = input.totals;
  const posture = inferCampaignPosture(quality, speed);
  const pack = CAMPAIGN_MIRROR[posture];
  const vi = pickVariant(`${posture}-${score}-${earned}`, 2);
  const [hook, upside, downside, practice] = pack[vi];

  const lines: string[] = [
    hook,
    upside,
    downside,
    practice,
    minigameFootnote(earned),
    `Табло (для сопоставления с текстом): очки ${score} · качество ${quality} · скорость ${speed}.`,
    "Другой набор веток в миссиях — другой профиль и другой разбор; это не «лучше», а «иначе».",
  ];
  return {
    title: "Свод кампании",
    lines,
  };
}

